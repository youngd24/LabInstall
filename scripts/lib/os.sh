typeset -f getOS


################################################################################
# NAME        : 
# DESCRIPTION :
# ARGS        :
# RETURNS     :
################################################################################
function getOS {
  UNAME=$(which uname)
  AWK=$(which awk)
  UNAMEOUT=`$UNAME -a`

  OSNAME=`echo $UNAMEOUT | $AWK '{print $1}'`
  return $OSNAME
}
