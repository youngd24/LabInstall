################################################################################
# NAME        : getOS
# DESCRIPTION : Gets the name of the OS
# ARGS        : N/A
# RETURNS     : string(name)
################################################################################
function getOS {
  UNAME=$(which uname)
  AWK=$(which awk)
  UNAMEOUT=`$UNAME -a`

  OSNAME=`echo $UNAMEOUT | $AWK '{print $1}'`
  return $OSNAME
}
